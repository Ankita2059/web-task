const student = {
    name: "Ankita",
    age: 20,
    grade: "A",
    subjects: ["Mathematics", "Physics", "Chemistry"],
    getDetails: function() {
        return `Name: ${this.name}, Grade: ${this.grade} `;
    }
};

// Example usage
console.log(student.getDetails()); // Output: Name: Ankita, Grade: A






